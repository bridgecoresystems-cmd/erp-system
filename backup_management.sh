        ls -lh "${BACKUP_DIR}"/factory_erp_backup_*.sql.gz 2>/dev/null | while read line; do
            echo "   $line"
        done
        
        echo ""
        read -p "–°–∫–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–Ω–∏—Ö —Ä–µ–∑–µ—Ä–≤–Ω—ã—Ö –∫–æ–ø–∏–π –æ—Å—Ç–∞–≤–∏—Ç—å? (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é 10): " keep_count
        keep_count=${keep_count:-10}
        
        if [[ "$keep_count" =~ ^[0-9]+$ ]] && [ "$keep_count" -gt 0 ]; then
            echo ""
            echo "üóëÔ∏è –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä—ã–µ —Ä–µ–∑–µ—Ä–≤–Ω—ã–µ –∫–æ–ø–∏–∏ (–æ—Å—Ç–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ $keep_count)..."
            
