            files_to_delete=$(ls -t "${BACKUP_DIR}"/factory_erp_backup_*.sql.gz 2>/dev/null | tail -n +$((keep_count + 1)))
            
            if [ -n "$files_to_delete" ]; then
                echo "$files_to_delete" | while read file; do
                    if [ -f "$file" ]; then
                        rm -f "$file"
                        echo "   ‚úÖ –£–¥–∞–ª–µ–Ω: $(basename "$file")"
                    fi
                done
                echo ""
                echo "‚úÖ –û—á–∏—Å—Ç–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
            else
                echo "   ‚ÑπÔ∏è –ù–µ—Ç —Ñ–∞–π–ª–æ–≤ –¥–ª—è —É–¥–∞–ª–µ–Ω–∏—è"
            fi
        else
            echo "‚ùå –ù–µ–≤–µ—Ä–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ñ–∞–π–ª–æ–≤"
        fi
    else
        echo "‚ùå –î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è —Å —Ä–µ–∑–µ—Ä–≤–Ω—ã–º–∏ –∫–æ–ø–∏—è–º–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω–∞"
    fi
